def matrix_entry_variables(w_alpha, variable_candidate_dict):
    """Return candidate variables attached to each possibly nonzero entry.

    Input:
        w_alpha: symbolic Weyl matrix after candidate construction.
        variable_candidate_dict: dict mapping candidate variables to values.
    Output:
        A dict keyed by matrix position (i, j).  The value is the list of
        candidate variables appearing in that entry.  Constant nonzero entries
        are retained with an empty variable list because a monomial support
        cannot turn them off.
    """
    candidate_vars = set(variable_candidate_dict)
    entry_vars = {}
    for i in range(w_alpha.rows):
        for j in range(w_alpha.cols):
            expr = w_alpha[i, j]
            if expr == 0:
                continue
            vars_in_entry = sorted(
                expr.free_symbols.intersection(candidate_vars),
                key=lambda s: s.name,
            )
            entry_vars[(i, j)] = vars_in_entry
    return entry_vars


def enumerate_monomial_entry_supports(matrix_size, entry_variables, variable_candidate_dict):
    """Yield entry supports with one selected position in each row and column.

    Input:
        matrix_size: size n of the n x n Weyl matrix.
        entry_variables: dict from matrix_entry_variables().
        variable_candidate_dict: dict mapping candidate variables to values.
    Output:
        Iterator of tuples of matrix positions.  Each tuple has length n and
        uses every row and every column exactly once.  Positions containing a
        variable whose candidate list forbids 0 are treated as forced.
    """
    positions_by_row = {i: [] for i in range(matrix_size)}
    forced_positions = set()

    for pos, vars_in_entry in entry_variables.items():
        row, _col = pos
        positions_by_row[row].append(pos)
        if any(0 not in variable_candidate_dict[var] for var in vars_in_entry):
            forced_positions.add(pos)

    forced_rows = {}
    forced_cols = {}
    for row, col in forced_positions:
        if row in forced_rows and forced_rows[row] != (row, col):
            return
        if col in forced_cols and forced_cols[col] != (row, col):
            return
        forced_rows[row] = (row, col)
        forced_cols[col] = (row, col)

    rows_by_branching = sorted(
        range(matrix_size),
        key=lambda row: len(positions_by_row[row]),
    )

    def search(row_index, used_cols, support):
        if row_index == len(rows_by_branching):
            yield tuple(support)
            return

        row = rows_by_branching[row_index]
        if row in forced_rows:
            choices = [forced_rows[row]]
        else:
            choices = positions_by_row[row]

        for pos in choices:
            _row, col = pos
            if col in used_cols:
                continue
            if col in forced_cols and forced_cols[col] != pos:
                continue
            yield from search(row_index + 1, used_cols.union({col}), support + [pos])

    yield from search(0, set(), [])


def add_elapsed_time(counters, key, elapsed_seconds):
    """Add elapsed time to one factored Weyl timing counter.

    Input:
        counters: a dict from new_weyl_factored_counters(), or None.
        key: timing counter name.
        elapsed_seconds: float number of seconds to add.
    Output:
        None. Mutates counters when counters is not None.
    """
    if counters is not None:
        counters[key] += elapsed_seconds


@contextmanager
def timed_stage(counters, key):
    """Measure one stage and add elapsed wall-clock time to counters.

    Input:
        counters: a dict from new_weyl_factored_counters(), or None.
        key: timing counter name.
    Output:
        Context manager that records elapsed time on exit.
    """
    start = time.perf_counter()
    try:
        yield
    finally:
        add_elapsed_time(counters, key, time.perf_counter() - start)


